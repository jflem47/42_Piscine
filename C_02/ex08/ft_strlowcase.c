#include <unistd.h>
#include <stdio.h>

char	*ft_strlowcase(char *str)
{
	int	counter;

	counter = 0;
	while (str[counter] != '\0')
	{
		if (str[counter] >= 65 && str[counter] < 91)
		{
			str[counter] += 32;
			counter++;
		}
		else
		{
			counter++;
			continue ;
		}
	}
	return (str);
}
